public class StockSumVo
{
	private BigDecimal qty;

	private BigDecimal money;

	public BigDecimal getQty()
	{
		return qty;
	}

	public void setQty(BigDecimal qty)
	{
		this.qty = qty;
	}

	public BigDecimal getMoney()
	{
		return money;
	}

	public void setMoney(BigDecimal money)
	{
		this.money = money;
	}

	public StockSumVo(BigDecimal qty, BigDecimal money)
	{
		super();
		this.qty = qty;
		this.money = money;
	}

	public StockSumVo()
	{
		super();
	}

}
