package com.huayin.printmanager.wx.vo;

import java.math.BigDecimal;

/**
 * <pre>
 * 微信 - 客户或供应商汇总VO
 * </pre>
 * @author       think
 * @version      1.0, 2017年11月26日 下午4:23:07
 * @since        2.0, 2018年2月26日 下午17:07:00, think, 规范和国际化
 */
public class SupOrCustSumVo
{
	private Long id;

	private String name;

	private BigDecimal money;

	public Long getId()
	{
		return id;
	}

	public void setId(Long id)
	{
		this.id = id;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public BigDecimal getMoney()
	{
		return money;
	}

	public void setMoney(BigDecimal money)
	{
		this.money = money;
	}

	public SupOrCustSumVo()
	{
		super();
	}

	public SupOrCustSumVo(Long id, String name, BigDecimal money)
	{
		super();
		this.id = id;
		this.name = name;
		this.money = money;
	}

}
