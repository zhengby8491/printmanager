	public static Map<String, Object> getMapFromXML(String xmlString)
			throws ParserConfigurationException, IOException, SAXException {

		// 这里用Dom的方式解析回包的最主要目的是防止API新增回包字段
		DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
		DocumentBuilder builder = factory.newDocumentBuilder();
		InputStream is = getStringStream(xmlString);
		Document document = builder.parse(is);

		// 获取到document里面的全部结点
		NodeList allNodes = document.getFirstChild().getChildNodes();
		Node node;
		Map<String, Object> map = new HashMap<String, Object>();
		int i = 0;
		while (i < allNodes.getLength()) {
			node = allNodes.item(i);
			if (node instanceof Element) {
				map.put(node.getNodeName(), node.getTextContent());
			}
			i++;
		}
		return map;
	}

	public static Map<String, Object> getAllMapFromXML(String xmlString)
			throws ParserConfigurationException, IOException, SAXException {

		// 这里用Dom的方式解析回包的最主要目的是防止API新增回包字段
		DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
		DocumentBuilder builder = factory.newDocumentBuilder();
		InputStream is = getStringStream(xmlString);
		Document document = builder.parse(is);

		// 获取到document里面的全部结点
		NodeList allNodes = document.getFirstChild().getChildNodes();
		Node node;
		Map<String, Object> map = new HashMap<String, Object>();
		int i = 0;
		while (i < allNodes.getLength()) {
			node = allNodes.item(i);
			if (node.getNodeName().equals("is_success")) {
				map.put(node.getNodeName(), node.getTextContent());
			} else if (node.getNodeName().equals("request")) {
				// 不处理
			} else if (node.getNodeName().equals("response")) {
				NodeList tradeNodes = node.getChildNodes().item(0).getChildNodes();
				int j = 0;
				while (j < tradeNodes.getLength()) {
					node = tradeNodes.item(j);
					map.put(node.getNodeName(), node.getTextContent());
					j++;
				}
			} else if (node.getNodeName().equals("sign")) {
				map.put(node.getNodeName(), node.getTextContent());
			} else if (node.getNodeName().equals("sign_type")) {
				map.put(node.getNodeName(), node.getTextContent());
			} else if (node.getNodeName().equals("error")) {
				map.put(node.getNodeName(), node.getTextContent());
			}
			i++;
		}
		return map;
	}

